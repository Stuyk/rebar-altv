export interface PedOptions {
    /**
     * Make the ped invincible
     *
     * @type {boolean}
     * @memberof PedOptions
     */
    invincible: boolean;

    /**
     * Make the ped do literally nothing when interacted with
     *
     * @type {boolean}
     * @memberof PedOptions
     */
    makeStupid: boolean;
}
